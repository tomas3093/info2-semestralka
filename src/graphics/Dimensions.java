package graphics;

/**
 * Prepravka definujuca rozmery. Nemenitelny objekt.
 */
public class Dimensions {

    public final int width;
    public final int height;

    /**
     * Vytvori rozmery.
     *
     * @param width Rozmer dlzka.
     * @param height Rozmer vyska.
     */
    public Dimensions(int width, int height) {
        this.width = width;
        this.height = height;
    }

}
